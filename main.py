import turtle

# we created an object called turtle
turtle = turtle.Turtle()

# defining a function for square

def square():
  turtle.forward(90)
  turtle.left(90)
  turtle.forward(90)
  turtle.left(90)
  turtle.forward(90)
  turtle.left(90)
  turtle.forward(90)
  turtle.left(90)

square()
turtle.forward(90)
square()